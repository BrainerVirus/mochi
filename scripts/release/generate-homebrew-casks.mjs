import { createHash } from "node:crypto";
import { createWriteStream } from "node:fs";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { pipeline } from "node:stream/promises";
import { fileURLToPath } from "node:url";

const DEFAULT_REPO = "BrainerVirus/mochi";
const DEFAULT_HOMEPAGE = `https://github.com/${DEFAULT_REPO}`;

export function caskVersionFromTag(tag) {
  if (/^v\d/.test(tag)) {
    return tag.slice(1);
  }
  return tag;
}

export function pickMacDmgAssets(release) {
  const assets = release.assets ?? [];
  const pick = (patterns) => {
    const matches = assets.filter((asset) => patterns.some((pattern) => pattern.test(asset.name)));
    if (matches.length === 0) {
      return null;
    }
    matches.sort((left, right) => (left.updated_at ?? "").localeCompare(right.updated_at ?? ""));
    const asset = matches.at(-1);
    return {
      name: asset.name,
      url: asset.browser_download_url,
    };
  };

  const arm64 = pick([/aarch64.*\.dmg$/i, /arm64.*\.dmg$/i]);
  const x64 = pick([/(?<![a-z])x64.*\.dmg$/i, /x86_64.*\.dmg$/i, /amd64.*\.dmg$/i]);

  if (!arm64 || !x64) {
    throw new Error(
      `release ${release.tag_name} is missing macOS dmg assets (arm64=${Boolean(arm64)}, x64=${Boolean(x64)})`,
    );
  }

  return { arm64, x64 };
}

export function renderCask({ id, name, desc, version, homepage = DEFAULT_HOMEPAGE, arm64, x64 }) {
  return `# Generated by scripts/release/generate-homebrew-casks.mjs. Do not edit manually.

cask "${id}" do
  version "${version}"

  on_arm do
    sha256 "${arm64.sha256}"
    url "${arm64.url}",
        verified: "github.com/${DEFAULT_REPO}/"
  end

  on_intel do
    sha256 "${x64.sha256}"
    url "${x64.url}",
        verified: "github.com/${DEFAULT_REPO}/"
  end

  name "${name}"
  desc "${desc}"
  homepage "${homepage}"

  app "Mochi.app"
end
`;
}

async function fetchRelease(repo, tag) {
  const headers = { Accept: "application/vnd.github+json" };
  if (process.env.GITHUB_TOKEN) {
    headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  }

  const response = await fetch(
    `https://api.github.com/repos/${repo}/releases/tags/${encodeURIComponent(tag)}`,
    { headers },
  );
  if (!response.ok) {
    throw new Error(`GitHub release lookup failed for ${tag}: ${response.status}`);
  }
  return response.json();
}

async function sha256File(path) {
  const hash = createHash("sha256");
  hash.update(await readFile(path));
  return hash.digest("hex");
}

async function downloadFile(url, destination) {
  const headers = {};
  if (process.env.GITHUB_TOKEN) {
    headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
  }

  const response = await fetch(url, { headers, redirect: "follow" });
  if (!response.ok) {
    throw new Error(`download failed (${response.status}): ${url}`);
  }

  await mkdir(dirname(destination), { recursive: true });
  await pipeline(response.body, createWriteStream(destination));
}

async function hashRemoteAsset(asset, cacheDir) {
  const destination = join(cacheDir, asset.name);
  await downloadFile(asset.url, destination);
  return {
    ...asset,
    sha256: await sha256File(destination),
  };
}

const CASK_DEFINITIONS = {
  "mochi-desktop": {
    name: "Mochi",
    desc: "Cross-platform desktop companion for AI coding tool usage",
  },
  "mochi-unstable": {
    name: "Mochi (Unstable)",
    desc: "Cross-platform desktop companion for AI coding tool usage (unstable channel)",
  },
};

export async function generateHomebrewCask({
  tag,
  caskId,
  repo = DEFAULT_REPO,
  outDir,
  cacheDir = join(dirname(fileURLToPath(import.meta.url)), ".homebrew-cache"),
}) {
  const definition = CASK_DEFINITIONS[caskId];
  if (!definition) {
    throw new Error(`unsupported cask id: ${caskId}`);
  }

  const release = await fetchRelease(repo, tag);
  release.tag_name = release.tag_name ?? tag;
  const assets = pickMacDmgAssets(release);
  const version = caskVersionFromTag(release.tag_name);

  const [arm64, x64] = await Promise.all([
    hashRemoteAsset(assets.arm64, cacheDir),
    hashRemoteAsset(assets.x64, cacheDir),
  ]);

  const filename = `${caskId}.rb`;
  const body = renderCask({
    id: caskId,
    name: definition.name,
    desc: definition.desc,
    version,
    arm64,
    x64,
  });

  await mkdir(outDir, { recursive: true });
  await writeFile(join(outDir, filename), body);

  return {
    tag: release.tag_name,
    caskId,
    version,
    filename,
  };
}

function parseArgs(argv) {
  const args = {
    tag: "",
    caskId: "",
    outDir: join(process.cwd(), "Casks"),
    repo: DEFAULT_REPO,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const value = argv[index];
    if (value === "--tag") {
      args.tag = argv[++index] ?? "";
      continue;
    }
    if (value === "--cask") {
      args.caskId = argv[++index] ?? "";
      continue;
    }
    if (value === "--out-dir") {
      args.outDir = argv[++index] ?? args.outDir;
      continue;
    }
    if (value === "--repo") {
      args.repo = argv[++index] ?? args.repo;
      continue;
    }
    if (value === "--help" || value === "-h") {
      args.help = true;
    }
  }

  return args;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help || !args.tag || !args.caskId) {
    console.log(`Usage: node scripts/release/generate-homebrew-casks.mjs --tag <tag> --cask <mochi-desktop|mochi-unstable> [--out-dir Casks]

Generates a Homebrew cask for macOS arm64 and x64 DMGs from a GitHub release tag.
`);
    process.exit(args.help ? 0 : 1);
  }

  const result = await generateHomebrewCask(args);
  console.log(
    `Wrote ${result.filename} for release ${result.tag} (cask version ${result.version}).`,
  );
}

if (import.meta.url === new URL(process.argv[1], "file:").href) {
  main().catch((error) => {
    console.error(error instanceof Error ? error.message : error);
    process.exit(1);
  });
}
